    @Override
    public void setData(int index) {
        intPosition = index;
        txtContador.setText(String.valueOf(intPosition + 1));
        txtCod.setText(String.valueOf(produtos.get(index).getCodigo()));
        txtDescricao.setText(produtos.get(index).getDescricao());
        String strPreco = String.valueOf(produtos.get(index).getPreco());
        int intPonto = strPreco.indexOf(".");
        String strTemp = strPreco.substring(intPonto + 1);
        if (strTemp.length() == 1) {
            strPreco += "0";
        }
        txtPreco.setText(strPreco);

        if (intPosition > 0 && intPosition < produtos.size() - 1) {
            btnPrimeiro.setEnabled(true);
            btnVoltar.setEnabled(true);
            btnProximo.setEnabled(true);
            btnUltimo.setEnabled(true);
        } else {
            if (intPosition == 0) {
                setEnabledNavigator(false);
            } else if (intPosition == produtos.size() - 1) {
                setEnabledNavigator(true);
            }
        }
    }

