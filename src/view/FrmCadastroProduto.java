    public void addProduto(ProdutoBean produto) {
        produtos.add(produto);
        lblContador.setText("de ( " + produtos.size() + " )");
        Object o[] = {
            produtos.get(produtos.size() - 1).getCodigo(),
            produtos.get(produtos.size() - 1).getDescricao(),
            produtos.get(produtos.size() - 1).getPreco()
        };
        defaultTableModel.addRow(o);
    }

    @Override
    public void atualizaToolbar(boolean novo) {
        lblContador.setText("de ( " + produtos.size() + " )");
        if (novo && !produtos.isEmpty()) {
            setData(produtos.size() - 1);
        } else if (!produtos.isEmpty()) {
            setData(intPosition);
        }
    }

    @Override
    public void begin() {
        try {
            mfPreco = new MaskFormatter("##,##");
            mfPreco.setPlaceholderCharacter('_');
            mfPreco.setValueContainsLiteralCharacters(true);
            mfPreco.setValueClass(String.class);
            DefaultFormatterFactory dffPreco = new DefaultFormatterFactory(mfPreco);
            txtPreco.setFormatterFactory(dffPreco);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um ParseException"
                    + " na aplicação do MaskFormatter: " + e.getMessage(), "Erro:",
                    JOptionPane.ERROR_MESSAGE);
        }

        defaultTableModel.addColumn("Cod.");
        defaultTableModel.addColumn("Produto");
        defaultTableModel.addColumn("Preço");
        try {
            ProdutoDao dao = new ProdutoDao();
            produtos = dao.pesquisar();
            for (int i = 0; i < produtos.size(); i++) {
                Object[] o = {
                    produtos.get(i).getCodigo(),
                    produtos.get(i).getDescricao(),
                    produtos.get(i).getPreco()
                };
                defaultTableModel.addRow(o);
            }
            atualizaTable();
            atualizaToolbar(true);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FrmCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FrmCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (!produtos.isEmpty()) {
            setData(0);
        }
    }

