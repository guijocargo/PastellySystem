    private void txtContadorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContadorKeyReleased
        performNavigateAction(evt);
        int intKeyCode = evt.getKeyCode();

        // Verifica se foi realmente digitado um número, caso não, o caractere 
        // digitado é apagado
        switch (intKeyCode) {
            case KeyEvent.VK_0:
                break;
            case KeyEvent.VK_1:
                break;
            case KeyEvent.VK_2:
                break;
            case KeyEvent.VK_3:
                break;
            case KeyEvent.VK_4:
                break;
            case KeyEvent.VK_5:
                break;
            case KeyEvent.VK_6:
                break;
            case KeyEvent.VK_7:
                break;
            case KeyEvent.VK_8:
                break;
            case KeyEvent.VK_9:
                break;
            case KeyEvent.VK_NUMPAD0:
                break;
            case KeyEvent.VK_NUMPAD1:
                break;
            case KeyEvent.VK_NUMPAD2:
                break;
            case KeyEvent.VK_NUMPAD3:
                break;
            case KeyEvent.VK_NUMPAD4:
                break;
            case KeyEvent.VK_NUMPAD5:
                break;
            case KeyEvent.VK_NUMPAD6:
                break;
            case KeyEvent.VK_NUMPAD7:
                break;
            case KeyEvent.VK_NUMPAD8:
                break;
            case KeyEvent.VK_NUMPAD9:
                break;
            default:
                txtContador.setText(txtContador.getText().replace(
                        evt.getKeyChar(), ' ').trim());
                break;
        }
        if (intKeyCode == KeyEvent.VK_ENTER) {
            setData(Integer.parseInt(txtContador.getText()) - 1);
        }
    }//GEN-LAST:event_txtContadorKeyReleased

