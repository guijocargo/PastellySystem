    private void tabProdutosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabProdutosKeyReleased
        performNavigateAction(evt);
        if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            if (booAlteracao) {
                btnCancelar.doClick();
                booAlteracao = false;
            } else if (booInclusao) {
                booInclusao = false;
            }
            setData(tabProdutos.getSelectedRow());
        } else if (evt.getKeyCode() == KeyEvent.VK_UP) {
            if (booAlteracao) {
                btnCancelar.doClick();
                booAlteracao = false;
            } else if (booInclusao) {
                booInclusao = false;
            }
            setData(tabProdutos.getSelectedRow());
        } else if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (booAlteracao) {
                btnCancelar.doClick();
                booAlteracao = false;
            } else if (booInclusao) {
                booInclusao = false;
            }
            setData(tabProdutos.getSelectedRow());
            btnAlterar.doClick();
        }
    }//GEN-LAST:event_tabProdutosKeyReleased

    private void tabProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabProdutosMouseClicked
        switch (evt.getClickCount()) {
            case 1:
                if (booAlteracao) {
                    btnCancelar.doClick();
                    booAlteracao = false;
                } else if (booInclusao) {
                    booInclusao = false;
                }
                setData(tabProdutos.getSelectedRow());
                break;
            case 2:
                if (booAlteracao) {
                    btnCancelar.doClick();
                } else if (booInclusao) {
                    booInclusao = false;
                }
                setData(tabProdutos.getSelectedRow());
                btnAlterar.doClick();
                break;
        }
    }//GEN-LAST:event_tabProdutosMouseClicked

