    /**
     * Carregar um ClienteBean contendo as informações de um único cliente
     * 
     * @param codUser Código do cliente a ser carregado
     * @return ClienteBean Um ClienteBean contendo as informações do cliente
     */
    public ClienteBean carregaBean(int codUser) {
        int intCliente = 0;
        if (!txtCod.getText().isEmpty()) {
            intCliente = Integer.parseInt(txtCod.getText());
        }
        ClienteBean bean = new ClienteBean(txtNome.getText(),
                txtCelular.getText(), txtData.getText(), txtTelefone.getText(),
                intCliente, codUser);
        return bean;
    }
