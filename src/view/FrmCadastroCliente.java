    @Override
    public void performNavigateAction(KeyEvent evt) {
        if (evt.isControlDown() && evt.isShiftDown()) {
            switch (evt.getKeyCode()) {
                case KeyEvent.VK_RIGHT:
                    btnUltimo.doClick();
                    break;
                case KeyEvent.VK_LEFT:
                    btnPrimeiro.doClick();
                    break;
            }
        } else if (evt.isControlDown()) {
            switch (evt.getKeyCode()) {
                case KeyEvent.VK_RIGHT:
                    btnProximo.doClick();
                    break;
                case KeyEvent.VK_LEFT:
                    btnVoltar.doClick();
                    break;
            }
        }
    }
